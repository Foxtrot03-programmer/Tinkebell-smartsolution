// PROGRESS BAR ANIMATION (scroll-triggered)
// ─────────────────────────────────────────────
const bars = document.querySelectorAll('.progress-fill');
const savedWidths = [];

bars.forEach(bar => {
  savedWidths.push(bar.style.width);
  bar.style.width = '0';
});

const progressObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      bars.forEach((bar, i) => {
        setTimeout(() => {
          bar.style.transition = 'width 1.2s ease';
          bar.style.width = savedWidths[i];
        }, i * 150);
      });
      progressObserver.disconnect();
    }
  });
}, { threshold: 0.3 });

const progressSection = document.querySelector('.how-visual');
if (progressSection) progressObserver.observe(progressSection);

// ─────────────────────────────────────────────
// CARD FADE-IN ANIMATION (scroll-triggered)
// ─────────────────────────────────────────────
const cards = document.querySelectorAll('.service-card, .why-card');

const cardObserver = new IntersectionObserver(entries => {
  entries.forEach((entry, i) => {
    if (entry.isIntersecting) {
      setTimeout(() => {
        entry.target.style.opacity = '1';
        entry.target.style.transform = 'translateY(0)';
      }, 60 * i);
      cardObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.1 });

cards.forEach(card => {
  card.style.opacity = '0';
  card.style.transform = 'translateY(20px)';
  card.style.transition = 'opacity .5s ease, transform .5s ease, border-color .3s, box-shadow .3s';
  cardObserver.observe(card);
});

